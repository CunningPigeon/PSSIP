<?php
// Задание 5: Работа со строками
$s1 = "Я лублю Беларусь";
$s2 = "Я учусь в Политехническом колледже.";

// 5.1 Определение длины строки s2
$length_s2 = strlen($s2);
echo "Длина строки s2: $length_s2 символов.<br>";

// 5.2 Проверка на наличие слова "Гродно" в строке s1
$contains_grodno = strpos($s1, "Гродно") !== false ? "да" : "нет";
echo "Слово 'Гродно' встречается в строке s1: $contains_grodno.<br>";

// 5.3 Выделение 6-го символа из строки s2 и определение его ASCII-кода
$sixth_char = mb_substr($s2, 5, 1, 'UTF-8'); // 6-й символ (индекс 5)
$ascii_code = ord($sixth_char);
echo "6-й символ в строке s2: '". $sixth_char ."', ASCII-код: $ascii_code.<br>";

// Задание 6: Пользовательская функция для расчетов
function calculate($a, $b, $c, $x) {
    return 1 / sqrt($a * $x * $x + $b * $x + $c);
}

// Пример использования функции
$a = 1;
$b = 2;
$c = 1;
$x = 3;
$result = calculate($a, $b, $c, $x);
echo "Исходные данные:<br>a= $a<br>b= $b<br>c= $c<br>x= $x<br>";
echo "Результат расчета: $result<br>";
?>
